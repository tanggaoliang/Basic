package new_start_2019;

public class IfElseTest {
    public static void main(String[] args) {
        int x = 30, y = 20;
        if (x < 20) {
            System.out.println("true");
        } else {
            System.out.println("false");
        }
        if (x == 30 && y == 20) {
            System.out.println(true);
        }
    }
}
