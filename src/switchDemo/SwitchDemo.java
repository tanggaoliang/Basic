package switchDemo;

public class SwitchDemo {
    public static void main(String[] args) {
        int i = 3;
        switch (i) {
            case 1:
                System.out.println("i is 1");
                break;
            case 2:
                System.out.println("i is 2");
                break;
            case 3:
                System.out.println("i is 3");
                break;
            default:
                System.out.println("i don't know");

        }
    }
}
